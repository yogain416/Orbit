export function isKoreanHoliday(dateStr) {
  return Object.prototype.hasOwnProperty.call(HOLIDAYS, dateStr)
}

export function getHolidayName(dateStr) {
  return HOLIDAYS[dateStr] || null
}

// 색상 클래스 결정 헬퍼.
// 우선순위: 공휴일·일요일 > 토요일 > 평일.
// `variant` 는 'text' | 'bg-red-cell' 등으로 호출처가 자유롭게 정함.
export function getDayColorClass(dateStr, dayOfWeek) {
  const isHoliday = isKoreanHoliday(dateStr)
  if (isHoliday || dayOfWeek === 0) return 'red'
  if (dayOfWeek === 6) return 'blue'
  return 'normal'
}
